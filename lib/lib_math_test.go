package lib

import (
	"testing"
)

func TestMinus(t *testing.T) {
	type args struct {
		a int
		b int
	}
	tests := []struct {
		name string
		args args
		want int
	}{
		{
			name: "empty slice",
			args: args{
				a: 5,
				b: 1,
			},
			want: 4,
		},
	}
	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			if got := Minus(tt.args.a, tt.args.b); got != tt.want {
				t.Errorf("Minus() = %v, want %v", got, tt.want)
			}
		})
	}
}

